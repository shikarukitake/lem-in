#include <stdlib.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	char		c_c;
	const char	*s_s;

	s_s = s;
	c_c = (char)c;
	while (n-- > 0)
	{
		if (*s_s == c_c)
			return ((void*)s_s);
		s_s++;
	}
	return (NULL);
}

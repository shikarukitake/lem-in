#include "libft.h"

int		ft_strchrn(const char *s, int c)
{
	int i;

	i = 0;
	if (c == '\0')
		return (-1);
	while (s[i])
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	return (-1);
}

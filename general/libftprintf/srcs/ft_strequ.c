int		ft_strequ(char const *s1, char const *s2)
{
	int i;

	i = 1;
	if (!s1 || !s2)
		return (0);
	if (!(*s1) && !(*s2))
		return (1);
	while (*s1 || *s2)
	{
		if (*s1 != *s2)
			i = 0;
		s1++;
		s2++;
	}
	return (i);
}

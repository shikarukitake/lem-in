#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2,
		size_t n)
{
	int i;

	if (!s1 || !s2)
		return (0);
	i = 1;
	if (!(*s1) && !(*s2))
		return (1);
	while (n-- > 0)
	{
		if (*s1 != *s2)
			i = 0;
		if (!(*s1) && !(*s2))
			break ;
		s1++;
		s2++;
	}
	return (i);
}
